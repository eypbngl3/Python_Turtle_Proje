import turtle as t



print(t.forward(100)) # ileri gitme 

print(t.right(90)) #90 derece sağa dönsün

print(t.fd(100)) # 100 daha ileri gitsin

print(t.right(90)) # 90 derece sağa dönsün

t.fd(100) # ileri gitme

print(t.backward(50)) # geri gitme
print(t.left(90)) # 90 derece sola

t.bk(50) # geri gitme


t.done()